
public class wideningAndNarrowing_05 {

	public static void main(String[] args) {
		//Boolean can not be converted to any other datatype.
		
		int a=10;
		byte b=(byte)a;//Narrowing
		//In case of narrowing typecasting is must
		//Narrowing causes loss of data
		
		//Conversion of widened data to narrower is called as narrowing. 
		
		//conversion of one preemptive datattype to another is called as type conversion
		//char -> int
		//float -> int
		
		
		
		
		byte p=15;
		int q=p;//widening
		//Converting narrower datatatype to wider datatype is called as widening
		
		
		int x=15;
		float y=x;//Type Conversion
		//In case of widening there is no need of typecasting.
		
		
		

	}

}
